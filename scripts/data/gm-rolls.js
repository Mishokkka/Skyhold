export function gmRollEntry(table, result) {
  const entries = table?.entries ?? {};
  if (Array.isArray(entries)) return entries[result % entries.length] ?? entries[0] ?? "—";
  return entries[result] ?? entries[String(result)] ?? Object.values(entries)[0] ?? "—";
}
